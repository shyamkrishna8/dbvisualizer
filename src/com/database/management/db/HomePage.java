    public void loadDatabases() {
        java.util.List<String> dbNames = InitDBConnector.INIT_DBCONNECTOR.getAllDatabases();
        jComboBox1.setModel(new DefaultComboBoxModel<>(dbNames.toArray(new String[dbNames.size()])));
        jComboBox1.setSelectedIndex(-1);
    }

    public void loadTables() {
        java.util.List<String> tables = InitDBConnector.INIT_DBCONNECTOR.getTables(String.valueOf(jComboBox1.getSelectedItem()));
        jComboBox2.setModel(new DefaultComboBoxModel<>(tables.toArray(new String[tables.size()])));
        jComboBox2.setSelectedIndex(-1);
    }

