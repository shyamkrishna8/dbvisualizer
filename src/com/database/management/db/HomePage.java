    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        String tableName = String.valueOf(jComboBox2.getSelectedItem());
        String whereClause = jTextArea1.getText();
        String sqlQuery = "SELECT * FROM " + tableName;
        if (whereClause != null && !whereClause.isEmpty()) {
            sqlQuery += " " + whereClause;
        }

        DynamicTableResults dtr = userSession.getResults(sqlQuery);

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setColumnIdentifiers(dtr.getHeaders());
        for (String[] row : dtr.getRows()) {
            dtm.addRow(row);
        }
        jTable1.setVisible(true);
        jButton5.setVisible(true);
    }//GEN-LAST:event_jButton3MouseClicked

